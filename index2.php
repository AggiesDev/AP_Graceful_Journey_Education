function initials(string $name): string {
    $parts = preg_split('/\s+/', trim($name));
    $chars = '';
    foreach ($parts as $part) {
        if ($part !== '') {
            $chars .= mb_strtoupper(mb_substr($part, 0, 1));
        }
        if (mb_strlen($chars) >= 2) {
            break;
        }
    }
    return $chars ?: 'AG';
}

function asset_exists(string $path): bool {
    return $path !== '' && file_exists(__DIR__ . DIRECTORY_SEPARATOR . $path);
}
